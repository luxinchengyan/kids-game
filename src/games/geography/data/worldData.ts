export const iso3ToCountry: Record<string, WorldCountry> = Object.fromEntries(
  worldCountries.map((c) => [c.id, c])
);

// 用于地图 Geography 属性名称映射
export const nameToCountry: Record<string, WorldCountry> = Object.fromEntries(
  worldCountries.flatMap((c) => [
    [c.name, c],
    [c.nameEn, c],
    [c.nameEn.toUpperCase(), c],
  ])
);

// 按难度分组
